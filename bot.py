            "–≠—Ö, —á—Ç–æ-—Ç–æ –º–æ–∏ —Å–æ–ª–Ω–µ—á–Ω—ã–µ —Å—Ö–µ–º—ã –∑–∞–ø—É—Ç–∞–ª–∏—Å—å...\n"

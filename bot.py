        f"‚ö° –û—Ä–∏–µ–Ω—Ç–∏—Ä–æ–≤–æ—á–Ω–∞—è –º–æ—â–Ω–æ—Å—Ç—å —Å—Ç–∞–Ω—Ü–∏–∏: ~{power_kw} –∫–í—Ç\n"
        f"üèó –ü—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ–º—ã–π —Ç–∏–ø —Å—Ç–∞–Ω—Ü–∏–∏: {station_type}\n"

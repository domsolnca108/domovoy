def extract_contact_info(text):
    """–ò–∑–≤–ª–µ–∫–∞–µ–º –∫–æ–Ω—Ç–∞–∫—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ —Å–æ–æ–±—â–µ–Ω–∏—è"""
    try:
        phone_pattern = r'(\+7|8)[\s\-]?\(?[0-9]{3}\)?[\s\-]?[0-9]{3}[\s\-]?[0-9]{2}[\s\-]?[0-9]{2}'
        phone_match = re.search(phone_pattern, text)
        
        name_pattern = r'(–º–µ–Ω—è –∑–æ–≤—É—Ç|–∏–º—è|–∑–≤–∞—Ç—å|–º–æ–µ –∏–º—è)[\s:]*([–ê-–Ø–∞-—è]{2,20})'
        name_match = re.search(name_pattern, text, re.IGNORECASE)
        
        return {
            'phone': phone_match.group(0) if phone_match else None,
            'name': name_match.group(2) if name_match else None
        }
    except Exception as e:
        logger.error(f"Error extracting contact info: {e}")
        return {'phone': None, 'name': None}

